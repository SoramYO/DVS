const calculateDiamondPrice = (carat, color, clarity) => {
  let weightRange;
  if (carat <= 0.03) weightRange = "0.01-0.03";
  else if (carat <= 0.07) weightRange = "0.04-0.07";
  else if (carat <= 0.14) weightRange = "0.08-0.14";
  else if (carat <= 0.17) weightRange = "0.15-0.17";
  else if (carat <= 0.22) weightRange = "0.18-0.22";
  else if (carat <= 0.29) weightRange = "0.23-0.29";
  else if (carat <= 0.39) weightRange = "0.30-0.39";
  else if (carat <= 0.49) weightRange = "0.40-0.49";
  else if (carat <= 0.69) weightRange = "0.50-0.69";
  else if (carat <= 0.89) weightRange = "0.70-0.89";
  else if (carat <= 0.99) weightRange = "0.90-0.99";
  else if (carat <= 1.49) weightRange = "1.00-1.49";
  else if (carat <= 1.99) weightRange = "1.50-1.99";
  else if (carat <= 2.99) weightRange = "2.00-2.99";
  else if (carat <= 3.99) weightRange = "3.00-3.99";
  else if (carat <= 4.99) weightRange = "4.00-4.99";
  else if (carat <= 5.99) weightRange = "5.00-5.99";
  else if (carat <= 6.99) weightRange = "6.00-6.99";
  else if (carat <= 7.99) weightRange = "7.00-7.99";
  else if (carat <= 8.99) weightRange = "8.00-8.99";
  else if (carat <= 9.99) weightRange = "9.00-9.99";
  else if (carat <= 10.99) weightRange = "10.00-10.99";

  const pricePerCarat = rapaportPrices[weightRange]?.[color]?.[clarity];
  if (!pricePerCarat) return 0;
  const totalPrice = pricePerCarat * 100 * carat;
  return totalPrice;
};

